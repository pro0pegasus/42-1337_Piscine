#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (nb > 0)
	{
		while (a * a <= nb)
		{
			if (a * a == nb)
				return (a);
			if (a > 46340)
				return (0);
			a++;
		}
	}
	return (0);
}
