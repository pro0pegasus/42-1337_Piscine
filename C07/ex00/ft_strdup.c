#include <stdlib.h>
#include <string.h>
#include <stdio.h>

int	ft_strlength(char *str)
{
	int	index;

	index = 0;
	while (str[index])
		index++;
	return (index);
}

char	*ft_strdup(char *src)
{
	int		index;
	char	*dest;
	char	*a;

	index = 0;
	a = ((dest = (char *)malloc(ft_strlength(src) * sizeof(char) + 1)));
	if (!a)
	{
		return (0);
	}
	while (src[index])
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}
