#include <unistd.h>

void	ft_print_comb(void)
{
	int	e;
	int	f;
	int	g;

	e = '0';
	while (e <= '7')
	{
		f = e + 1;
		while (f <= '8')
		{
			g = f + 1;
			while (g <= '9' )
			{
				write (1, &e, 1);
				write (1, &f, 1);
				write (1, &g, 1);
				if (e != '7')
					write (1, ", ", 2);
					g++;
			}
			f++;
		}
		e++;
	}
}
